        },
            ruleForm: {
					lingjianName: '',
					lingjianUuidNumber: this.getUUID(),//数字
					lingjianPhoto: '',
					lingjianTypes: '',//数字
					lingjianValue: '',//数字对应的值
					lingjianKucunNumber: '',
					lingjianNewMoney: '',
					lingjianClicknum: '',
					lingjianContent: '',
					lingjianDelete: '',
					insertTime: '',
