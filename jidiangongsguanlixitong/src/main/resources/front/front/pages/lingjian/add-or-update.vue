    import wPicker from "@/components/w-picker/w-picker.vue";

    export default {
        data() {
            return {
                cross: '',
                ro:{
					lingjianName: false,
					lingjianUuidNumber: true,
					lingjianPhoto: false,
					lingjianTypes: false,
