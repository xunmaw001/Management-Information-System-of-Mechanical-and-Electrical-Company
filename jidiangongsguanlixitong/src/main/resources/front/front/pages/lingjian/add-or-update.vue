            // 登陆用户信息
            user: {},
            lingjianTypesOptions: [],
            lingjianTypesIndex : 0,

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
		/*下拉框*/
			let lingjianTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'lingjian_types',
            }
			let lingjianTypes = await this.$api.page(`dictionary`, lingjianTypesParams);
			this.lingjianTypesOptions = lingjianTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`lingjian`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.lingjianId){
                this.ruleForm.lingjianId = options.lingjianId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            lingjianTypesChange(e) {
                this.lingjianTypesIndex = e.target.value
                this.ruleForm.lingjianValue = this.lingjianTypesOptions[this.lingjianTypesIndex].indexName
                this.ruleForm.lingjianTypes = this.lingjianTypesOptions[this.lingjianTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			lingjianPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.lingjianPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.lingjianName)) {
                    this.$utils.msg(`机电零件名称不能为空`);
                    return
                }
				if ((!this.ruleForm.lingjianUuidNumber)) {
                    this.$utils.msg(`机电零件编号不能为空`);
                    return
                }
				if ((!this.ruleForm.lingjianPhoto)) {
                    this.$utils.msg(`机电零件照片不能为空`);
                    return
                }
				if ((!this.ruleForm.lingjianKucunNumber) && (!this.$validate.isIntNumer(this.ruleForm.lingjianKucunNumber)) && this.ruleForm.lingjianKucunNumber >0) {
                    this.$utils.msg(`机电零件库存不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.lingjianClicknum) && (!this.$validate.isIntNumer(this.ruleForm.lingjianClicknum)) && this.ruleForm.lingjianClicknum >0) {
                    this.$utils.msg(`机电零件热度不能为空，不能小于0 格式为数字`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`lingjian`, this.ruleForm);
                } else {
                    await this.$api.save(`lingjian`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
</script>
